package com.example.authentication.component;

import io.jsonwebtoken.Claims;
import io.jsonwebtoken.Jwts;
import io.jsonwebtoken.SignatureAlgorithm;
import jakarta.servlet.http.Cookie;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.stereotype.Component;

import java.util.Date;
import java.util.HashMap;
import java.util.Map;
import java.util.function.Function;

@Component
public class JwtComponent {

    @Value("${spring.jwt.secret}")
    private String secret;

    @Value("${spring.jwt.key}")
    private String cookieKey;

    @Value("${spring.jwt.path}")
    private String cookiePath;

    @Value("${spring.jwt.httpOnly}")
    private Boolean httpOnly;

    @Value("${spring.jwt.expiry}")
    private Integer expiry;

    public String extractUsername(String token) {
        return extractClaim(token, Claims::getSubject);
    }

    public Date extractExpiration(String token) {
        return extractClaim(token, Claims::getExpiration);
    }

    public <T> T extractClaim(String token, Function<Claims, T> claimsResolver) {
        final Claims claims = extractAllClaims(token);
        return claimsResolver.apply(claims);
    }

    private Claims extractAllClaims(String token) {
        return Jwts.parser().setSigningKey(secret).parseClaimsJws(token).getBody();
    }

    private Boolean isTokenExpired(String token) {
        return extractExpiration(token).before(new Date());
    }

    public String generateToken(String payload) {
        Map<String, Object> claims = new HashMap<>();
        return createToken(claims, payload);
    }

    private String createToken(Map<String, Object> claims, String subject) {
        return Jwts.builder().setClaims(claims).setSubject(subject).setIssuedAt(new Date(System.currentTimeMillis()))
                .setExpiration(new Date(System.currentTimeMillis() + 1000 * 60 * 60 * 10))
                .signWith(SignatureAlgorithm.HS256, secret).compact();
    }

    public Boolean validateToken(String token, String username) {
        final String extractedUsername = extractUsername(token);
        return (extractedUsername.equals(username) && !isTokenExpired(token));
    }

    public Cookie getJwtCookie(String payload) {
        Cookie cookie = new Cookie(cookieKey, generateToken(payload));
        cookie.setPath(cookiePath);
        cookie.setHttpOnly(httpOnly);
        cookie.setMaxAge(expiry);
        return cookie;
    }
}